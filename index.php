 <p>
 Mengenal jurusan teknologi komputer
Teknologi komputer itu sendiri merupakan ilmu yang mempelajari seputar perangkat komputer baik itu software maupun hardware. Teknologi komputer itu sendiri tidak bisa lepas dari yang namanya informasi, sebab pada informasi itu terdapat sesuatu yang jadi tujuan dengan penyampaian dan penjabaran yang sebenarnya. Teknologi komputer juga membahas mengenai sistem jaringan dan telekomunikasi, sebab komputer merupakan sebuah bidang yang juga sangat penting dalam kehidupan manusia.
Prospek kerja lulusan teknologi komputer
Seperti dalam siaran pers, UniVersitas Bina Sarana Infromatika, dengan menekankan segi analisis bisnis dan manajemen, lulusan Teknologi Komputer sangat cocok untuk masuk sebagai ahli IT perusahaan. Kemampuan menggabungkan bisnis dengan programming sering menjadi kendala bagi seorang programmer yang lebih fokus ke sisi teknis seperti lulusan Teknik Informatika maupun Ilmu Komputer. Berikut prospek kerja prodi Teknologi Komputer:
