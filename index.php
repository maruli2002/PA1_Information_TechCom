			<h3>1. Android Programer</h3>

Untuk menjadi seorang programer itu kita harus menguasai bahasa pemrograman Java atau yang lainnya. Sebagai lulusan Teknologi Komputer anda harus memiliki bekal untuk menjadi android developer. Selain itu juga harus bisa berkolaborasi dengan UX Desainer dan mendapatkan aplikasi yang mudah digunakan oleh pengguna.

<h3>2. Software Engineer</h3>

Software Engineer biasanya mengembangkan perangkat lunak menjadi berbagai macam keperluan yang dapat digunakan untuk mendukung pekerjaan atau yang lainnya. Dalam pengembangan software, seorang engineer harus benar-benar memiliki skill bagus. Tugas utama profesi ini adalah mengembangkan program aplikasi yang sebelumnya telah dibuat sang programmer. Jika diibaratkan, programmer itu membuat program awal, lalu dikembangkan oleh Software Engineer

<h3>3. Konsultan IT</h3>

Seorang lulusan Ilmu Komputer/Teknologi Komputer juga dapat menjadi IT Consultant/Konsultan IT, dengan demikian ia dapat menerapkan ilmu yang ia dapatkan pada prodi Teknik Informatika yang dimilikinya secara nyata, terutama dalam hal perencanaan, pelaksanaan, serta evaluasi dalam teknologi dan informasi.

Secara spesifik, konsultan IT bisa menjadi spesialis teknis yang fokus dalam mengintegrasikan teknologi informasi dalam bisnis dan memberi saran atau konsultasi pada klien dalam penggunaan teknologi informasi supaya penggunaannya efisien dan sesuai target.

<h3>4. Computer Network atau Data Comunication Engineer</h3>

Seorang lulusan Ilmu Komputer (Computer Science) atau Teknik Informatika (Informatics) dan teknologi komputer dapat menjadi ahli computer network dimana dapat merancang arsitektur dan pembangunan jaringan. Seorang Computer Network juga berperan besar dalam perawatan dan pengelolaan jaringan dalam suatu perusahaan.

Banyak yang beranggapan bahwa profesi ini cukup mudah. Tetapi, pada kenyataannya cukup sulit dan tidak semua lulusan jurusan TI bisa melakukannya.Contoh kecil adalah, ketika data communication engineer diminta untuk menaikkan kecepatan internet di sebuah perusahaan.

<h3>5. Game Developer</h3>

Pada pekerjaan Game Developer ini juga memerlukan keahlian dari lulusan informatika, terutama ia yang mahir grafis. Untuk membuat game, harus dilakukan secara tim karena proyek ini bisa dikatakan cukup berat. Ada yang bagian membuat animasi grafis, ada yang bagian programming, dan lainnya. Bagi Anda yang kuliah prodi Teknik Informatika dan suka sekali dengan yang namanya games, tidak ada salahnya anda membuat games. Selain mendapatkan uang, anda bisa memainkan games buatan anda sendiri.

Itulah beberapa prospek kerja cemerlang bagi lulusan Infromatika dan Teknologi Komputer. Seorang lulusan Informatika pastinya akan mampu berkarya dan menunjukan kemampuannya untuk hasil terbaik bagi teknologi yang perkembangannya sangat luas.

Ia juga bisa menciptakan beberapa teknologi melalui perangkat lunak komputer sehingga hasilnya bisa dirasakan oleh orang banyak. Sekarang mencari kampus atau universitas yang menyediakan jursan teknologi komputer juga sudah banyak, baik itu universitas negeri maupun swasta. Salah satu Universias yang menyediakan prodi Teknologi Komputer adalah Univesitas BSI (Bina Sarana Informatika).</p>
